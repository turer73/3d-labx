## Tasarim ve Yapi Kalitesi

X1 Carbon''un tasarimi, 3D yazici dunyasinda bir donum noktasidir. Tam kapali kasa yapisi sadece estetik degil, fonksiyonel bir tercihtir. ABS ve ASA gibi sicakliga hassas malzemelerde cevre sicakligini kontrol altinda tutarak warping (bukuelme) sorununu minimuma indiriyor.

### Kasa ve Cerceve
Metal cerceve yaklasik 14 kg agirliginda saglam ve titresime dayanikli. CoreXY kinematik sistemi, hafif baskikafalariyla yuksek hiz ve ivmelenme degerlerine ulasmayi mumkun kiliyor. Kapaklarin cam panelleri baski sirasinda iceriyi izlemenize olanak taniyor.

### Baski Tablasi
Cift tarafli PEI kapli celik tabla manyetik olarak yerine oturuyor. Bir tarafi puetuerlu (textured) diger tarafi puezsuez yuezeyli. PLA ve PETG icin puetuerlue taraf muekemmel yapisma saglarken, TPU gibi esnek malzemeler icin puezsuez taraf daha uygun.

### Hava Filtreleme
Dahili aktif karbon filtre, ABS ve ASA baskisi sirasinda ortaya cikan VOC gazlarini buyuk oelcuede filtreliyor. Kapali bir odada calisirken bile koku neredeyse hissedilmiyor — bu ozellikle ev ortaminda baski yapanlar icin cok oenemli.

### Aydinlatma
IC led aydinlatma sistemi, baski sirasinda objeyi esit sekilde aydinlatarak kamera goeruentuesueuenuen kalitesini artiriyor. Gece baskilarinda bile net goeruentue alinabiyor.

