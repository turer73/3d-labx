## Baski Kalitesi Testleri

3 aylik test sueremizde X1 Carbon''u farkli malzemeler ve zorluk seviyelerinde test ettik.

### PLA Testleri (Bambu PLA Basic)
- **Sicaklik:** 220°C nozzle / 60°C tabla
- **Hiz:** 300 mm/s
- **Sonuc:** Muekemmel yuezeye kalitesi. Katman cizgileri 0.2mm''de neredeyse goerueenmuez. Koeprue testlerinde 60mm''ye kadar sarkmadan baski yapabildi. Overhang testlerinde 70 dereceye kadar desteksiz temiz sonuc.

### PETG Testleri (Bambu PETG HF)
- **Sicaklik:** 245°C nozzle / 80°C tabla
- **Hiz:** 250 mm/s
- **Sonuc:** PETG''nin bilinen stringing (iplik cekme) sorunlari X1 Carbon''da minimum seviyede. Retraction ayarlari fabrika cikindan optimize edilmis. Mekanik parcalarda guecluek ve esneklik dengesi cok iyi.

### ABS/ASA Testleri
- **Sicaklik:** 260°C nozzle / 100°C tabla (kapali kasa)
- **Hiz:** 200 mm/s
- **Sonuc:** Kapali kasa tasarimi ABS baskida devrim yaratmis. 20cm boyundaki parcalarda bile warping yok denecek kadar az. ASA ile dis mekan parcalari basarili sekilde bastirildi. Aktif karbon filtre koku sorununu bueyuek oelcuede coezueyoer.

### TPU Testleri (95A Shore)
- **Sicaklik:** 230°C nozzle / 60°C tabla
- **Hiz:** 80 mm/s (TPU icin)
- **Sonuc:** Direct drive ekstrueder TPU''yu sorunsuz besledi. Bowden sistemlerde yasanan tikinma ve besleme sorunlari burada yok. Esnek telefon kilifi ve titresim damperleri basariyla basildi.

### Karbon Fiber Takviyeli Malzemeler (PA-CF)
- **Sicaklik:** 280°C nozzle / 100°C tabla
- **Hiz:** 150 mm/s
- **Sonuc:** Hardened steel nozzle ile PA-CF baskisi oezellikle etkileyici. Endustriyel kalitede sertlik ve dayaniklilik. Drone parcalari ve mekanik baglanti elemanlarinda kullandik — sonuclar muekemmel.

