### Input Shaping
Yueeksek hizlarda en bueyuek duesman titresimdir ve titresim = ghosting/ringing artefaktlari demektir. X1 Carbon''un ivmeoeolcer tabanli input shaping sistemi titresimi aktif olarak kompanze ediyor. 300 mm/s''de bile ghosting neredeyse goerueluemez seviyede.

### Guerueltue Seviyesi
Yueeksek hiza ragmen kasa icindeki sessizlik etkileyici. Olcuemlerimizde:
- Bekleme modunda: ~25 dB
- Normal baski (100mm/s): ~45 dB
- Yueeksek hiz (300mm/s): ~52 dB
- Maksimum hiz (500mm/s): ~58 dB

Gece baskisi icin 300 mm/s''ye kadar rahatlikla kullanilabilir.

